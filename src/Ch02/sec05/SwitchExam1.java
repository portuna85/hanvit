package Ch02.sec05;

public class SwitchExam1 {

	public static void main(String[] args) {
		int num = 3;
		
		switch (num) {
		
		case 1 :
			System.out.println("Good Morning, JAVA");
			break;
		case 2 :
			System.out.println("Good Aafernoon, JAVA");
			break;
		case 3 :
			System.out.println("Good Evening, JAVA");
			break;
		default :
			System.out.println("Good Night, JAVA");
			break;
		}
		System.out.println("Done");
		
		}
		
		

	}


